void MapManager::LoadTileset(const std::string& prefix, int count) {
    tileSet.clear();
    for (int i = 0; i < count; i++) {
        std::string name = prefix + "_" + std::to_string(i); // Looks for "tile_0", "tile_1"...
        Sprite s = ResourceManager::GetSprite(name);
        tileSet.push_back(s);
    }
}

int MapManager::GetTile(float worldX, float worldY)
{
    int x = (int)(worldX / cellSize);
    int y = (int)(worldY / cellSize);
    if (x >= 0 && x < mapWidth && y >= 0 && y < mapHeight) return grid[y][x];
    return -1;
}

void MapManager::Draw(SDL_Renderer* renderer, glm::vec2 cam) {
    for (int y = 0; y < mapHeight; y++) {
        for (int x = 0; x < mapWidth; x++) {
            
            int tileID = grid[y][x];

            if (tileID >= 0 && tileID < tileSet.size()) { // make sure the ID exists
                Sprite& s = tileSet[tileID];

                if (s.texture) {
                    SDL_Rect destRect = { (int)(x * cellSize - cam.x), (int)(y * cellSize - cam.y), (int)cellSize, (int)cellSize };
                    SDL_RenderCopy(renderer, s.texture, &s.sourceRect, &destRect);
                    continue;
                }
            }
            SDL_SetRenderDrawColor(renderer, 255, 0, 255, 255);
            SDL_Rect destRect = {(int)(x*cellSize), (int)(y*cellSize), (int)cellSize, (int)cellSize};
            SDL_RenderFillRect(renderer, &destRect);
        }
    }
}